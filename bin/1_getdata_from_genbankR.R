    for (i in seq_along(family_names)) {
      seq_species[[i]] <- read.GenBank (list_species[[i]], 
                                        seq.names = list_species[[i]],
                                        species.names = F,   
                                        as.character = T)
      names(seq_species[[i]]) <- list_species_names[[i]]
      mkdir(family_names[[i]])
      write.dna(seq_species[[i]], paste0("data/families", family_names[[i]], "/", species_names[i],".fasta"), format = "fasta")
    }
  }
}
